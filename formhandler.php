  <!-- Payment Method Selection -->
  <div>
    <label>
      <input type="radio" name="payment_method" value="card" checked>
      Credit/Debit Card
    </label>
    <br>
    <label>
      <input type="radio" name="payment_method" value="paypal">
      PayPal
    </label>
  </div>
  
  <!-- Credit/Debit Card Information -->
  <div id="card_info">
    <input type="text" name="card_number" placeholder="Card Number">
    <input type="text" name="card_exp" placeholder="Expiration (MM/YY)">
    <input type="text" name="card_cvc" placeholder="CVC">
  </div>
  
  <!-- PayPal Information -->
  <div id="paypal_info" style="display:none;">
    <input type="text" name="paypal_email" placeholder="PayPal Email">
  </div>
  
  <!-- Submit Button -->
  <input type="submit" value="Pay Now">
</form>

<!-- JavaScript for toggling the payment method -->
<script>
  var paymentMethod = document.querySelector('input[name="payment_method"]:checked').value;
  document.querySelectorAll('input[name="payment_method"]').forEach(function(el) {
    el.addEventListener('change', function() {
      paymentMethod = this.value;
      if (paymentMethod === 'card') {
        document.getElementById('card_info').style.display = 'block';
        document.getElementById('paypal_info').style.display = 'none';
      } else {
        document.getElementById('card_info').style.display = 'none';
        document.getElementById('paypal_info').style.display = 'block';
      }
    });
  });
</script>
